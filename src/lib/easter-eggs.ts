/**
 * Централизованный файл пасхалок и мемов.
 * Только клиентские компоненты используют pickRandom() —
 * серверные компоненты берут статические строки напрямую.
 */

export function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

